void solve() {
    int n;
    cin >> n;
    vector<int> a(n);

    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    if (n != 2) {
        NO;
        return;
    }

    sort(all(a));

    if (a[1] - a[0] != 1) {
        YES;
    } else {
        NO;
    }
}

